
public class Circle extends Shape {
public xxcircle myobj=new xxcircle();

	void setLocation(int a) {
		myobj.locationset(a);
		}
	void getLocation(int a) {
		myobj.locationget(a);
		}
	void fill() {
		myobj.fillcolour();
		}
	void display() {
		myobj.disp();
		}
	void setColour() {
		myobj.colourset();
		}
}
